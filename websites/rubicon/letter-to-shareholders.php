                       <h5 class="blue">Emerging industries have always been subject to market volatility, and this was the case for the sapphire industry and for Rubicon in 2012. After the high-growth period of 2010 and early 2011, the sapphire market slowed in late 2011. Demand for two- and four-inch diameter sapphire declined sharply, and even after unit volume recovered, pricing for two-inch material remained unattractively low throughout 2012. Rubicon and other major sapphire suppliers had added capacity during a period of strong market growth, but demand for LED sapphire for the television backlighting market began to level off while the expected growth from the LED general illumination market was delayed.</h5>
                
                <p>Fortunately, because of our leadership position, Rubicon’s sales of six-inch sapphire wafers continued to be relatively strong during 2012. We held our smaller diameter products off the market for a period of time to see whether pricing would recover. This shifted our sales mix further in the direction of large-diameter wafers for the year. However, low factory utilization and the weak pricing environment, particularly for smaller diameter products, put significant pressure on margins. For fiscal year 2012, Rubicon posted revenue of $67.2 million compared with 2011 revenue of $134.0 million, and a loss of $5.5 million for 2012 as compared to net income of $38.1 million in 2011.</p>

<p>Although industry capacity for sapphire exceeded overall demand in 2012, we expect that the continuing growth of the LED industry, fueled by the accelerating adoption of solid-state lighting, will absorb the existing sapphire capacity over the coming quarters. IMS Research estimates that the overall LED market will reach $13.9 billion, with the lighting market nearly doubling to $5.8 billion, in three years.</p>

<p>Meanwhile, the commercial success of Silicon-on-Sapphire (SoS) Radio Frequency Integrated Circuits (RFIC), now prevalent in smart phones and other consumer devices, resulted in strong demand for our sapphire wafers and represents a significant growth area going forward as new applications debut.</p>

<p>Our vertically integrated manufacturing capabilities remain central to our strategy and enable the continuous process improvement and innovation that allow Rubicon to maintain its leadership position in quality and innovation. Vertical integration is also key to achieving cost efficiencies and control of sapphire production, allowing us to scale production and provide our customers with a reliable supply of high-quality sapphire products that meet their unique and exacting specifications.</p>


<p>Our focus on the end-to-end manufacturing process and continuous innovation drove a number of milestones for Rubicon during 2012—achievements that further our quest to improve quality, reduce cost and serve more markets.</p>

<p>We solidified our position as the leading global provider of large diameter polished wafers due to our strength in both large-diameter crystal growth and wafer polishing. Underscoring this position, in January 2013 we announced that Rubicon has shipped over 400,000 polished six-inch wafers into the LED and SoS markets to date, far exceeding any of our competitors. We also have high volume eight-inch wafer production capability and have demonstrated twelve-inch wafer capability. Industry experts expect the use of larger diameter sapphire substrates to grow significantly in coming years. Research firm Yole Developpement forecasts that by 2017, six-inch sapphire wafers will comprise 49% of the sapphire market for LEDs. Given our strength in both large-diameter crystal growth and wafer polishing, we expect to remain the preferred supplier for large-diameter polished sapphire wafers and should benefit more than any other supplier from the transition to larger diameters.</p>

<p>In 2012, Rubicon was issued two U.S. patents for our post-crystal-growth technology. The first covered Rubicon’s equipment and process developed to perform in-situ orientation of sapphire crystals. The second was for our process to perform continuous conditioning and self-optimization of polishing equipment to achieve consistent, ultra-flat and defect-free surface quality for high-volume, large-diameter wafers.</p>

<p>Sapphire’s physical and optical qualities offer many benefits in applications such as military infrared windows, but existing methods used to produce large, high-quality sapphire windows do not meet all the demands of military applications. Drawing on its vast experience, Rubicon developed a new crystal growth platform to address this void. This research sets the stage for both defense and industrial applications for large sapphire windows. The ground-breaking project, dubbed Large-Area Net-shape Crystal Extraction (LANCE), is supported by the Air Force Research Laboratory (AFRL), with a total value of $4.7 million over three years.</p>


<p>In 2012, we continued to refine our vertical integration by incorporating a raw material preparation process, and we advanced our capability to produce patterned sapphire substrates. Our emerging patterning capability is moving forward and we expect to be the first to offer large-diameter patterned sapphire substrates for use in the high-brightness LED market later this year.</p>

<p>Developments such as these, along with ongoing improvements to our wafer fabrication processes, will increase throughput and reduce cost while maintaining the industry’s highest standards, further bolstering our competitive advantage.</p>

<p>Moving forward, we remain excited about the high growth markets we serve. LED-based general illumination is finally gaining global traction, with multiple product and geographic markets being penetrated by many suppliers. Prices of LED devices are falling, lumens per dollar are rising, product quality is improving, and payback periods for commercial applications are more compelling each day. The SoS market is growing nicely as SoS RF chips continue to displace the legacy solutions. And new applications for sapphire continue to emerge. I believe our intellectual property for sapphire crystal growth and fabrication and our vertical integration make Rubicon the unparalleled leader in high-quality sapphire products and position us to benefit as these emerging markets grow. </p>

